  List<Question> getByCategory(QuizCategory category) {
    return _allQuestions.where((q) => q.category == category).toList();
  }

  List<Question> getRandomQuestions(QuizCategory category, int count) {
    final questions = getByCategory(category)..shuffle();
    return questions.take(count).toList();
  }

  List<Question> getAllShuffled(int count) {
    final all = List<Question>.from(_allQuestions)..shuffle();
    return all.take(count).toList();
  }
